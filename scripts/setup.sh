    echo "âŒ uv not found"
    read -p "Install uv now? (Y/n): " -n 1 -r
    echo ""
    if [[ ! $REPLY =~ ^[Nn]$ ]]; then
        curl -LsSf https://astral.sh/uv/install.sh | sh
        export PATH="$HOME/.cargo/bin:$PATH"
    else
        echo "âš ï¸  Install uv manually: curl -LsSf https://astral.sh/uv/install.sh | sh"
        exit 1
    fi
